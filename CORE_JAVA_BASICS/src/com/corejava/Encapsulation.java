package com.corejava;

public class Encapsulation {

	private int id;
	private int age;
	private String name;

	public void setId(int id) {
		this.id = id;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public void SetName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public int getAge() {
		return age;
	}

	public String getName() {
		return name;
	}
}
