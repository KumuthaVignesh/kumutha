package com.corejava;

public class SumofNaturalNum {
	public static void main(String args[])
	{
		int n=50,sum=0;
		for(int i=1;i<n;i++)
		{
			sum=sum+i;
					}
		System.out.println(sum);
	
		int nn=10,j=1,sum1=0;
		while(j<nn)
		{
			sum1=sum1+j;
			j++;
					}
		System.out.println(sum1);
		
	}

}